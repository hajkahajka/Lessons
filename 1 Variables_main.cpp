#include <iostream>

using namespace std;

int main() {
    /**
     * Base type of variables
     */
    int a = 3; // 16 || 32 bit
    double d = 5.4; // 32 bit
    bool g = true; // 8 bit

    /**
     * variables of int family
     */
    short s = 3; // 16 bit
    long l = 7; // 32 bit
    char c = 't'; // 8 bit

    /**
     * variables of double family
     */
    float f = 4.5; // 4 bit

    /**
     * unice type of var (mass of @char)
     */
    string ss = "Vital'ka hui";
    return 0;
}
